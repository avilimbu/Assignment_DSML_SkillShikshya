# Create a list of five favourite fruits. Add one more fruit using append() and print the updated list.


#creating a list of five favourite fruits
Friuts = ["apple", "mango", "orange", "banana", "cherry"]

#Adding one more fruit using append
Friuts.append("pineapple")

#printing the updated list
print(Friuts)


#OUTPUT

# ['apple', 'mango', 'orange', 'banana', 'cherry', 'pineapple']